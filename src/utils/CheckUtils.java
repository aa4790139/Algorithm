package utils;

public class CheckUtils {
    //-------------------------------------------------------------------------
    private CheckUtils(){

    }
    //-------------------------------------------------------------------------
    public static boolean isSort(Integer[] arr) {
        for (int i = 1; i < arr.length; i++) {
            if (arr[i - 1] > arr[i]) {
                return false;
            }
        }
        return true;
    }
    //-------------------------------------------------------------------------
}
